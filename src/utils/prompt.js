// ── Monta a mensagem do usuário para a API ─────────────────
export function buildUserMessage(context, tone, objetivo) {

  const toneMap = {
    divertido:  'DIVERTIDO',
    provocante: 'PROVOCANTE',
    cachorrão:  'CACHORRÃO',
  }

  const objetivoMap = {
    puxar:     'PUXAR ASSUNTO',
    flertar:   'FLERTAR',
    encontro:  'MARCAR ENCONTRO',
    reengajar: 'REENGAJAR',
  }

  const toneLabel    = toneMap[tone]     || 'DIVERTIDO'
  const objetivoLabel = objetivoMap[objetivo] || 'PUXAR ASSUNTO'

  const contextPart = context?.trim()
    ? `\n\nContexto extra fornecido: "${context.trim()}"`
    : ''

  return `Analise a conversa nessa imagem.

OBJETIVO: ${objetivoLabel}
TOM: ${toneLabel}
${contextPart}

Com base no objetivo e no tom acima, gere 3 respostas estratégicas.
Use o FORMATO OBRIGATÓRIO com RESPOSTA 1, RESPOSTA 2, RESPOSTA 3.`
}
