// ── Monta a mensagem do usuário para a API ─────────────────
export function buildUserMessage(context, tone, girlName) {
  const toneMap = {
    divertido: 'DIVERTIDO',
    provocante: 'PROVOCANTE',
    cachorrão: 'CACHORRÃO',
  }

  const toneLabel = toneMap[tone] || 'DIVERTIDO'

  const namePart = girlName?.trim()
    ? `\n\nNome dela: ${girlName.trim()} — use o nome nas respostas quando soar natural.`
    : ''

  const contextPart = context?.trim()
    ? `\n\nContexto extra fornecido: "${context.trim()}"`
    : ''

  return `Analise a conversa e gere 3 respostas no tom ${toneLabel}.${namePart}${contextPart}

Lembre: use o FORMATO OBRIGATÓRIO com RESPOSTA 1, RESPOSTA 2, RESPOSTA 3.`
}
